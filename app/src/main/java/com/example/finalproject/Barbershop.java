package com.example.finalproject;

public class Barbershop {

    String barber_name, barber_address, barber_distance;
    int imageId;

    public Barbershop(String barber_name, String barber_address, String barber_distance, int imageId) {
        this.barber_name = barber_name;
        this.barber_address = barber_address;
        this.barber_distance = barber_distance;
        this.imageId = imageId;
    }
}
